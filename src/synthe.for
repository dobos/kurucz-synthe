      IF(HWDOP.GE.HWSTK.AND.HWDOP.GE.HWLOR)GO TO 31
      NWID=2
      IF(HWLOR.GE.HWSTK)GO TO 31
      NWID=3
   31 HFWID=FREQNM*AMAX1(HWDOP,HWLOR,HWSTK)
