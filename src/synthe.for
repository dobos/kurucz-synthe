	halfwtot=wtot/2.
        if(il.eq.8)dp=0.4*dp
C     TOTAL SHIFT IN ANGSTROM, then in nm
      DTOT=W*D+Wp*dp+Whe*dhe
      dtot=dtot*0.1
C     VOIGT PARAMETERS
