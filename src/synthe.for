      RETURN
      END
      FUNCTION HFNM(N,M)
C     CALCULATES HYDROGEN OSCILLATOR STRENGTHS
      DATA NSTR/0/,MSTR/0/
      HFNM=0.
      IF(M.LE.N)RETURN
      IF(N.EQ.NSTR)GO TO 10
      XN=N
      GINF=.2027/XN**.71
      GCA=.124/XN
      FKN=XN*1.9603
      WTC=.45-2.4/XN**3*(XN-1.)
      NSTR=N
      GO TO 15
   10 IF(M.EQ.MSTR)GO TO 20
   15 XM=M
      XMN=M-N
      FK=FKN*(XM/(XMN*(XM+XN)))**3
      XMN12=XMN**1.2
      WT=(XMN12-1.)/(XMN12+WTC)
      FNM=FK*(1.-WT*GINF-(.222+GCA/XM)*(1.-WT))
      MSTR=M
   20 HFNM=FNM
      RETURN
      END
