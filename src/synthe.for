      D=X*shift(IT,il)+(1.0-X)*shift(IT-1,il)
      Dp=X*shiftp(IT,il)+(1.0-X)*shiftp(IT-1,il)
      Dhe=X*shifthe(IT,il)+(1.0-X)*shifthe(IT-1,il)
