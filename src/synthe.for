C      IF(N.NE.1)RESONT=RESONT+HFNM(1,N)/XN/(1.-1./XN2)*XN/3.*.791*2.
C     2 IS FOR CONVERTING XNFPH TO XNFH
C     RESONT=RESONT*5.593E-24/GNM*2.
C     RESONT=RESONT*5.593E-24/GNM
