C     DO DOPPLER
C     PUT FINE STRUCTURE IN DOPPLER CORE
   32 DO 33 I=1,IFINS
      D=ABS(FREQ-FREQNM-FINEST(I))/DOP
C     IF(D.LE.7.)HPROF4=HPROF4+EXP(-D*D)/1.77245/DOP*FINSWT(I)
