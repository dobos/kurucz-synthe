C     not Lyman alpha
   48 HHW=FREQNM*HWLOR
      TOP=HHW
      IF(N.NE.1.OR.M.GT.5)GO TO 49
C     Lyman beta
      IF(M.EQ.3.AND.FREQ.GT..885*3.288051D15.AND.
     1 FREQ.LT..890*3.288051D15)TOP=HHW-FREQNM*HWRAD
