     1CUTOFFH2(1)
      ELSE
      ICUT=(FREQ-FREQ22000)/SPACING
      CUTFREQ=ICUT*SPACING+FREQ22000
      CUTOFF=(CUTOFFH2(ICUT+2)-CUTOFFH2(ICUT+1))/SPACING*
     1(FREQ-CUTFREQ)+CUTOFFH2(ICUT+1)
      ENDIF
